public class OperadoresDeAtribuicao {
	
	public static void main (String[] args) {
		//1, 3, 2, 6, 3, 1, 2 e 1
		
		int a = 1; 
		System.out.println(a); //1
		
		a += 2; 
		System.out.println(a); //3
		
		a -= 1;
		System.out.println(a); //2
		
		a *= 3;
		System.out.println(a); //6
		
		a /= 2;
		System.out.println(a); //3
		
		a %= 2; //3/2 tem resto = 1
		System.out.println(a); //1
		
		a++;
		System.out.println(a); //2
		
		a--;
		System.out.println(a); //1
	}
}

